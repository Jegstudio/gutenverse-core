			'900'    => '900',
		);

		return apply_filters( 'gutenverse_font_weight', $weights );
	}

	/**
	 * Get Font Settings
	 *
	 * @return mixed
	 */
	public function get_font_styles() {
		$styles = array(
			'normal' => esc_html__( 'Normal', '--gctd--' ),
			'italic' => esc_html__( 'Italic', '--gctd--' ),
		);

		return apply_filters( 'gutenverse_font_styles', $styles );
	}

	/**
	 * Get Font Settings
	 *
	 * @return mixed
	 */
	public function get_font_transform() {
		$styles = array(
			'uppercase'  => esc_html__( 'Uppercase', '--gctd--' ),
			'capitalize' => esc_html__( 'Capitalize', '--gctd--' ),
			'lowercase'  => esc_html__( 'Lowercase', '--gctd--' ),
			'none'       => esc_html__( 'None', '--gctd--' ),
		);

		return apply_filters( 'gutenverse_font_transform', $styles );
	}

	/**
	 * Get Font Settings
	 *
	 * @return array
	 */
	public function get_font_settings() {
		$data              = array();
		$data['fonts']     = array();
		$data['groups']    = array();
		$data['weights']   = $this->get_font_weights();
		$data['styles']    = $this->get_font_styles();
		$data['transform'] = $this->get_font_transform();

		$fonts = $this->get_fonts();
		if ( isset( $fonts ) ) {
			foreach ( $fonts as $font => $group ) {
				$data['fonts'][] = array(
					'class' => $group,
					'value' => $font,
					'name'  => $font,
				);
			}
		}

		$groups = $this->get_font_groups();
		foreach ( $groups as $id => $group ) {
			$data['groups'][] = array(
				'value' => $id,
				'label' => $group,
			);
		}

		return $data;
	}
