		$weights = array(
			'normal' => esc_html__( 'Normal', '--gctd--' ),
			'bold'   => esc_html__( 'Bold', '--gctd--' ),
			'100'    => '100',
			'200'    => '200',
			'300'    => '300',
			'400'    => '400',
			'500'    => '500',
			'600'    => '600',
